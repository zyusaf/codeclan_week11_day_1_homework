public class Calculator {

    public Calculator() {
    }

    public int canAdd(int number1, int number2){
        return number1 + number2;
    }

    public int canSubtract(int number1, int number2){
        return number1 - number2;
    }

    public int canMultiply(int number1, int number2){
        return number1 * number2;
    }

    public double canDivide(double number1, double number2){
        return number1 / number2;
    }


}
